# Dictionaries:
# Key & Value pair 
# Each key must be unique

customer = {
    'name': 'Afaq',
    'age':24,
    'verified_email':True
}

print(customer['name'])
print(customer['age'])
print(customer['verified_email'])

# update the value for a key
customer['name']='Afaq Ahmad'
print(customer['name'])

# Add another key and value:
customer['email']='afaqahmad8849@gmail.com'
print(customer['email'])

