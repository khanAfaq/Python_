# For loop to itrate it to specfic number of times
# Syntext: For x/name/items in 'String'/list etc: do operations

print('\n')
for afaq in 'Systems':
    print(afaq)

print('\n')
for items in ['Afaq', 'Ahmad','Ali','John']:
    print(items)

print('\n')
for items in range(10):
    print(items)

print('\n')
# from specfic range:
for items in range(1,5):
    print(items)

print('\n')
for items in range(2,10):
    print(items)

print('\n')
# from specfic range 3 steps:
for items in range(2,10,3):
    print(items)

